function parseQuantity(value: unknown): number {
    if (typeof value === 'number') {
        return Number.isFinite(value) ? value : 0;
    }

    if (typeof value !== 'string') return 0;

    const easternToWestern: Record<string, string> = {
        '٠': '0', '١': '1', '٢': '2', '٣': '3', '٤': '4',
        '٥': '5', '٦': '6', '٧': '7', '٨': '8', '٩': '9'
    };

    const normalized = value
        .trim()
        .replace(/[٠-٩]/g, (ch) => easternToWestern[ch] || ch)
        .replace(/[٬,]/g, '')
        .replace(/٫/g, '.');

    const parsed = Number(normalized);
    return Number.isFinite(parsed) ? parsed : 0;
}

function formatQuantity(value: number): string {
    return new Intl.NumberFormat('ar-EG', {
        minimumFractionDigits: Number.isInteger(value) ? 0 : 2,
        maximumFractionDigits: 4
    }).format(value);
}

async function calculateNcrHoldRemainingQty(
    ncr: NcrRecord,
    companyId?: string | null
): Promise<{ reservedQty: number; sortedQty: number; remainingQty: number }> {
    const reservedQty = parseQuantity(ncr.reservedQty);
    if (reservedQty <= 0) {
        return { reservedQty: 0, sortedQty: 0, remainingQty: 0 };
    }

    let sortedQtyFromLogs = 0;
    let hasSortLogs = false;

    let query = supabase
        .from('ncr_hold_sort_logs')
        .select('sorted_qty')
        .eq('ncr_id', ncr.id);

    if (companyId) {
        query = query.eq('company_id', companyId);
    }

    const { data, error } = await query;

    if (!error && Array.isArray(data) && data.length > 0) {
        hasSortLogs = true;
        sortedQtyFromLogs = data.reduce((sum, row: any) => sum + parseQuantity(row?.sorted_qty), 0);
    }

    const legacySortedQty = (ncr.holds || []).reduce((sum, hold: any) => {
        return sum + parseQuantity(hold?.quantity ?? hold?.qty);
    }, 0);

    const sortedQty = hasSortLogs ? sortedQtyFromLogs : legacySortedQty;
    const remainingQty = Math.max(0, reservedQty - sortedQty);

    return { reservedQty, sortedQty, remainingQty };
}

function formatDate(date: string): { year: number } {
    const d = new Date(date);
    return { year: d.getFullYear() };
}

export async function generateNextNcrNumber(targetDate: string, maxRetries = 3): Promise<string> {
    const { year } = formatDate(targetDate);
    const counterKey = `ncr_counter_${year}`;

    for (let attempt = 1; attempt <= maxRetries; attempt++) {
        try {
            // Try atomic increment using RPC or direct SQL
            // First, try to get and increment atomically using upsert with conflict handling
            const { data: existingData } = await supabase
                .from(META_TABLE)
                .select('id, sequences')
                .eq('id', 'ncrCounters')
                .single();

            let sequences: Record<string, number> = existingData?.sequences || {};
            const currentValue = sequences[year] ?? 0;
            const nextValue = currentValue + 1;
            sequences[year] = nextValue;

            // Use upsert with optimistic locking pattern
            const { error: upsertError } = await supabase
                .from(META_TABLE)
                .upsert({
                    id: 'ncrCounters',
                    sequences,
                    updated_at: new Date().toISOString()
                }, {
                    onConflict: 'id'
                });

            if (upsertError) {
                // If conflict or error on last attempt, throw
                if (attempt === maxRetries) {
                    throw upsertError;
                }
                // Otherwise wait a bit and retry
                await new Promise(resolve => setTimeout(resolve, 50 * attempt));
                continue;
            }

            // Verify we got a unique number by checking if it was already used
            const paddedNumber = String(nextValue).padStart(4, '0');
            const ncrNumber = `NCR-${year}-${paddedNumber}`;

            const { data: existingNcr } = await supabase
                .from(NCR_TABLE)
                .select('id')
                .eq('number', ncrNumber)
                .single();

            if (existingNcr) {
                // Number already exists, retry
                if (attempt === maxRetries) {
                    throw new Error(`NCR number ${ncrNumber} already exists after ${maxRetries} attempts`);
                }
                continue;
            }

            return ncrNumber;

        } catch (err) {
            const errorMessage = (err as any)?.message || '';
            const errorCode = (err as any)?.code || '';
            // If meta table missing or RLS prevents insert, fall back immediately to timestamp-based number
            if (
                errorCode === 'PGRST205' ||
                errorMessage.includes("Could not find the table 'public.meta'") ||
                errorCode === '42501'
            ) {
                const fallbackNumber = `NCR-${year}-${Date.now().toString().slice(-6)}`;
                console.warn(`[NCR] META unavailable (code ${errorCode}), using fallback number ${fallbackNumber}`);
                return fallbackNumber;
            }
            if (attempt === maxRetries) {
                console.error('Failed to generate NCR number after retries:', err);
                throw err;
            }
            // Wait and retry
            await new Promise(resolve => setTimeout(resolve, 100 * attempt));
        }
    }

    // Fallback: generate with timestamp suffix for uniqueness
    const fallbackNumber = `NCR-${year}-${Date.now().toString().slice(-6)}`;
    console.warn(`Using fallback NCR number: ${fallbackNumber}`);
    return fallbackNumber;
}

async function uploadAttachments(ncrId: string, files: File[] = []): Promise<NcrAttachment[]> {
    if (!files.length) return [];

    const uploads = files.map(async (file) => {
        const path = `attachments/${ncrId}/${Date.now()}-${file.name}`;

        const { error: uploadError } = await supabase.storage
            .from('ncr-attachments')
            .upload(path, file);

        if (uploadError) {
            console.error('Upload error:', uploadError);
            throw uploadError;
        }

        const { data: urlData } = supabase.storage
            .from('ncr-attachments')
            .getPublicUrl(path);

        const attachment: NcrAttachment = {
            id: crypto.randomUUID(),
            fileName: file.name,
            storagePath: path,
            downloadURL: urlData.publicUrl,
            uploadedAt: new Date().toISOString()
        };

        return attachment;
    });

    return Promise.all(uploads);
}


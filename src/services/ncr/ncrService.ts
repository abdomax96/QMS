export async function getNcrById(id: string, companyId?: string | null): Promise<NcrRecord | null> {
    let query = supabase
        .from(NCR_TABLE)
        .select(NCR_SELECT_COLUMNS)
        .eq('id', id);

    if (companyId) {
        query = query.eq('company_id', companyId);
    }

    const { data, error } = await query.single();

    if (error || !data) return null;
    return normalizeNcr(data);
}

export async function fetchNcrs(companyId?: string | null): Promise<NcrRecord[]> {
    let query = supabase
        .from(NCR_TABLE)
        .select(NCR_SELECT_COLUMNS)
        .order('created_at', { ascending: false })
        .limit(200);

    if (companyId) {
        query = query.eq('company_id', companyId);
    }

    const { data, error } = await query;

    if (error) throw error;
    return (data || []).map(normalizeNcr);
}


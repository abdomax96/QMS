/**
 * Approve or reject root cause analysis
 */
export async function reviewRootCause(
    id: string,
    approved: boolean,
    reviewedBy: string,
    reviewedByName: string,
    reviewedByEmail: string,
    reviewedByRole: 'department' | 'quality',
    rejectionReason?: string,
    companyId?: string | null
): Promise<NcrRecord> {
    const ncr = await getNcrById(id, companyId);
    if (!ncr || !ncr.rootCauseApproval) throw new Error('NCR or root cause approval not found');

    const now = new Date().toISOString();

    const updatedApproval = {
        ...ncr.rootCauseApproval,
        status: approved ? 'approved' : 'rejected',
        reviewedBy,
        reviewedByName,
        reviewedByEmail,
        reviewedByRole,
        reviewedAt: now,
        ...((!approved && rejectionReason) ? { rejectionReason } : {})
    };

    const updates: Record<string, unknown> = {
        root_cause_approval: updatedApproval,
        updated_at: now
    };

    // If approved, progress to next stage
    if (approved && ncr.currentStage === 'root_cause_analysis') {
        updates.current_stage = 'capa_planning';
        updates.completed_stages = [...(ncr.completedStages || []), 'root_cause_analysis'];
    }

    let query = supabase.from(NCR_TABLE).update(updates).eq('id', id);
    if (companyId) {
        query = query.eq('company_id', companyId);
    }
    const { error } = await query;
    if (error) throw error;
    return (await getNcrById(id, companyId))!;
}

